// Language management
const i18n = {
    _lang: localStorage.getItem('bnl-lang') || 'ru',
    _listeners: [],

    get lang() {
        return this._lang;
    },

    setLang(lang) {
        this._lang = lang;
        localStorage.setItem('bnl-lang', lang);
        document.documentElement.lang = lang;
        this._listeners.forEach(fn => fn(lang));
    },

    t(key) {
        const dict = translations[this._lang] || translations.ru;
        return dict[key] || translations.ru[key] || key;
    },

    onChange(fn) {
        this._listeners.push(fn);
        return () => {
            this._listeners = this._listeners.filter(f => f !== fn);
        };
    }
};

// Set initial lang attribute
document.documentElement.lang = i18n.lang;

// Global shortcut
function t(key) {
    return i18n.t(key);
}

// React hook for language
function useLang() {
    const [lang, setLang] = React.useState(i18n.lang);

    React.useEffect(() => {
        return i18n.onChange(setLang);
    }, []);

    return lang;
}

